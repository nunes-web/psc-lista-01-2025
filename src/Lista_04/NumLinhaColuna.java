package Lista_04;

public class NumLinhaColuna {
  public static void main(String[] args) {
    // Programa para imprimir os números de 1 a 20, em coluna
    for (int i = 1; i <= 20; i++) {
      System.out.println(i);
    }

    // Programa para imprimir os números de 1 a 20, em linha
    for (int i = 1; i <= 20; i++) {
      System.out.print(i + ", ");
    }
  }
}
